# Exercício 3 Escreva um programa que leia dois números, calcule a diferença entre eles, mostre o resultado na tela.

numero1 = float(input("Digite o primeiro número: "))
numero2 = float(input("Digite o segundo número: "))
diferenca = numero1 - numero2

print(f"A diferença entre {numero1} e {numero2} é igual a {diferenca}")
