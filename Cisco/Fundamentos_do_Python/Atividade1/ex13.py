# Exercício 13 Faça um algoritmo que leia três notas de um aluno e calcule a média final deste aluno.

nota1 = float(input("Digite a primeira nota: "))
nota2 = float(input("Digite a terceira nota: "))
nota3 = float(input("Digite a terceira nota: "))
media_final = (nota1 + nota2 + nota3) / 3

print(f"A média final do aluno é: {media_final:.2f}")